package eu.dreamix.team.model.joke;

public class Joke {
	
	private Integer id;
	private String joke;
	
	public Joke() {
	}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getJoke() {
		return joke;
	}
	public void setJoke(String joke) {
		this.joke = joke;
	}
	
	
}
